package methodex;

public class MethodCalculator {

	public int add(int n1, int n2) {
		int result = n1+n2;
		return result;
	}
	
	public int sub(int n1, int n2) {
		int result= n1-n2;
		
		return result;
	}
	
	public int mul(int n1, int n2) {
		int result= n1*n2;
		return result;
	}
	
	public int div(int n1, int n2) {
		int result= n1/n2;
		return result;
	}
	
	public float div(float n1, float n2) {
		float result= n1/n2;
		return result;
	}
	
	public double div(double n1, double n2) {
		double result= n1/n2;
		return result;
	}
	
	public int share(int n1, int n2) {
		int result= n1%n2;
		return result;
	}
	
	public float share(float n1, float n2) {
		float result= n1%n2;
		return result;
	}
	
	public double share(double n1, double n2) {
		double result= n1%n2;
		return result;
	}
}
