package forex;

public class ForEx1 {

	public static void main(String[] args) {
		for(int i=2; i<11; i+=2) {
			System.out.println("i :"+i);
		}
		for(int i=0; i<11; i++) {
			System.out.print(i);
		}
		System.out.println();
		for(int i=0; i<11; i++) {
			System.out.print(i+" ");
		}

	}
}